int mobilePickup = 0;
void lift(int upSpeed, int downSpeed, int hold,  int upSpeedAuto, int distance, int height, int timer)
{
	clearTimer(T2);
	if(vexRT[Btn5U] == 1)
	{
		liftControl(upSpeed);
		mobilePickup = 0;
	}
	else if(vexRT[Btn5D] == 1)
	{
		liftControl(downSpeed);
		mobilePickup = 0;
	}
	else if(vexRT[Btn7U] == 1)
	{
		liftControl(upSpeed);
		mobilePickup = 1;
	}
	else if(vexRT[Btn7D] == 1)
	{
		liftControl(downSpeed+40);
		mobilePickup = 1;
	}
	else if(vexRT[Btn8R] == 1)
	{
		while(SensorValue[towerPing] <= distance && time1[T2] < timer)
		{
			liftControl(upSpeedAuto);
		}
	}
	else if(vexRT[Btn8L] == 1)
	{
		while(SensorValue[liftPot] <= height && time1[T2] < timer)
		{
			liftControl(upSpeedAuto);
		}
		while(SensorValue[liftPot] >= (height+300) && time1[T2] < timer)
		{
			liftControl(-upSpeedAuto);
		}
	}
	else
	{
		if(SensorValue[liftPot] <= 1400 && mobilePickup == 0)
		{
			liftControl(hold-10);
		}
		else if(mobilePickup == 1)
		{
			liftControl(hold+15);
		}
		else
		{
			liftControl(hold);
		}
	}
}

void clawMove(int openSpeed, int closeSpeed, int hold)
{

		if(vexRT[Btn6U] == vexRT[Btn6D])
		{
			motor[claw] = hold;
		}
		else if(vexRT[Btn6U] == 1 && SensorValue[clawPot] >= 2400)
		{
			motor[claw] = openSpeed;
		}
		else if(vexRT[Btn6D] == 1  && SensorValue[clawPot] <= 3000)
		{
			clearTimer(T3);
			if(time1[T3] < 1000)
			{
				motor[claw] = closeSpeed;
			}
		}
		else
		{
			motor[claw] = (hold - 10);
		}

}

